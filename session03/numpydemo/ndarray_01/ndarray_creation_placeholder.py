# creating array with placeholder

import numpy as np


# creating array by with default dtype
# zero()
a = np.zeros((3,4))
print(a)
print()


# creating array by with specified dtype
# zero()
a = np.zeros((3,4), dtype=int)
print(a)
print()


# creating array by with default dtype
# ones()
a = np.ones((3,4))
print(a)
print()


# creating array by with specified dtype
# ones()
a = np.ones((3,4), dtype=int)
print(a)
print()


# creating array by with default dtype
# empty()
a = np.empty((3,3))
print(a)
print()



