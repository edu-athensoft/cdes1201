# basic operations
# arithmetic operation
# elementwise

import numpy as np


a = np.array([20, 30, 40, 50])
b = np.array([1, 2, 3, 4])

# adding
c = a + b
print(c)

# subtracting
c = a - b
print(c)

# multiply
c = a * b
print(c)

# division
c = a / b
print(c)

# floor division
c = a // b
print(c)

